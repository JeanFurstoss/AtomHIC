#!/bin/bash

### Run ATOMHIC test and check results

function usage()
{
    echo "Run test and check results"
    echo ""
    echo "./AtomHic_test.sh"
    echo "-h             -- display this help"
    echo "-e             -- test executable full path"
    echo "-c             -- comparison tool executable full path"
    echo "-r             -- results filenames list"
    echo "-i             -- input directory containing reference results files"
    echo "-t             -- tolerance to compare values of results and reference files"
    echo ""
}

# Default values
ATOMHIC_EXECUTABLE=
ATOMHIC_COMPARISON_EXECUTABLE=
ATOMHIC_RESULTS_FILES=
ATOMHIC_INPUT_DIRECTORY=""
TOLERANCE=0.001

# Parse arguments of this script
if [ $# -lt 2 ]; then
    usage; exit 1;
fi
while getopts ":e:c:r:i:t:" opt
do
    case $opt in
        e ) ATOMHIC_EXECUTABLE=$OPTARG;;
        c ) ATOMHIC_COMPARISON_EXECUTABLE=$OPTARG;;
        r ) ATOMHIC_RESULTS_FILES=$OPTARG;;
        i ) ATOMHIC_INPUT_DIRECTORY=$OPTARG;;
        t ) TOLERANCE=$OPTARG;;
        * ) usage; exit 1;;
    esac
done

# Check if AtomHic test executable exists
ATOMHIC_EXECUTABLE_FULL_PATH=$(command -v $ATOMHIC_EXECUTABLE)
if [ ! -x "$ATOMHIC_EXECUTABLE_FULL_PATH" ]; then
    printf "ATOMHIC executable $ATOMHIC_EXECUTABLE does not exist \n"
    exit 1
fi

# Ouput files generated by this script
log_file="test.log"
diffnum_error_file="diffnum.err"
job_log_files="test_has_*"
out_files="output*"

# Clean
rm -f $log_file $diffnum_error_file $job_log_files $out_files

# Command
printf "Launch test : $ATOMHIC_EXECUTABLE\n"
($ATOMHIC_EXECUTABLE 2>&1 | tee $log_file) && (touch "test_has_succeeded") || (touch "test_has_failed")

# Refresh output directory
ls > /dev/null

# Initializing of test (comparison between results and reference files)
test=succeed

if [ -f "test_has_failed" ]; then
    test=failed
    printf "Error during execution (check test.log)"
elif [ "$ATOMHIC_COMPARISON_EXECUTABLE" != "" ]; then
    for RESULT_FILE in $ATOMHIC_RESULTS_FILES
    do
	RESULT_FILE=${RESULT_FILE//\"}
	RESULT="$RESULT_FILE"
	if [ -f "$RESULT" ]; then
	    RESULT_PATH=./$RESULT_FILE
	    RESULT_PATH=${RESULT_PATH%/*}
	    if [ "$RESULT_PATH" == "." ]; then
		RESULT_PATH=
	    else
		RESULT_PATH=${RESULT_FILE%/*}/
	    fi
	    REFERENCE_FILE=${RESULT_PATH}Ref_$(basename $RESULT_FILE)
	    REFERENCE="$ATOMHIC_INPUT_DIRECTORY/$REFERENCE_FILE"
	    #printf "input dir ref resIII $SON_EXECUTABLE" | tee "testvars2.txt"

	    printf "Comparison launched : $ATOMHIC_COMPARISON_EXECUTABLE $RESULT $REFERENCE $TOLERANCE\n" 
	    DIFFERENCE=`$ATOMHIC_COMPARISON_EXECUTABLE $RESULT $REFERENCE $TOLERANCE`
	    if [[ $DIFFERENCE != "" ]]; then
		test=failed
		printf "Difference between $RESULT_FILE and $REFERENCE_FILE :\n$DIFFERENCE" | tee $diffnum_error_file
		break
	    else
		printf "Comparison between $RESULT_FILE and $REFERENCE_FILE => Success\n"
	    fi
	else
	    test=failed
	    printf "Error during execution. (result file $RESULT_FILE not created)" | tee $diffnum_error_file
	    break
	fi
    done
fi

if [ "$test" = succeed ]; then
# Test succeed
    exit 0
else
# Test failed
    exit 1
fi
